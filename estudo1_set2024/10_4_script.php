<h1>Include e Require</h1>

<h3 style="color: red">include</h3>
Inclui e avalia o arquivo especificado durante a execução do script. Se o arquivo não
for encontrado, o PHP gera um warning (aviso) mas continua a execução do script. <br><br>

<h3 style="color: red">include_once</h3>
Similar ao include, mas garante que o arquivo seja incluído apenas uma vez durante a
execução do script. Se o arquivo já foi incluído antes, ele será ignorado na próxima vez. <br><br>

<h3 style="color: red">require</h3>
Funciona de maneira semelhante ao include, mas se o arquivo não forencontrado,

<h3 style="color: red">require_once</h3>
Funciona como require, mas garante que o arquivo seja incluído e avaliado apenas uma vez,
mesmo se chamado várias vezes.